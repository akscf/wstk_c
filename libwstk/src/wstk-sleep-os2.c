#include <wstk-sleep.h>
#include <wstk-log.h>

// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
// public
// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
wstk_status_t wstk_msleep(uint32_t msec) {
    if(!msec) {
        return WSTK_STATUS_SUCCESS;
    }

    DosSleep(msec);
    return WSTK_STATUS_SUCCESS;
}
